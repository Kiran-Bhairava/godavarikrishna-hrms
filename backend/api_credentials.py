async def login_enhanced(
    email: str,
    password: str,
    db: asyncpg.Connection,
) -> dict:
    """
    Enhanced login flow with credential validation.
    
    This should REPLACE the existing login() function.
    
    Changes:
    1. Check must_reset_password flag
    2. Log login attempt (success/failure)
    3. Return flag to force password reset
    """
    email_lower = email.lower()
    
    user = await db.fetchrow(
        """SELECT id, email, password_hash, full_name, role, is_active,
                  must_reset_password
           FROM users WHERE email = $1""",
        email_lower,
    )
    
    # Log failed attempt if user not found
    if not user:
        await db.execute(
            """INSERT INTO login_audits
               (email, success, failure_reason, created_at)
               VALUES ($1, FALSE, 'user_not_found', NOW())""",
            email_lower,
        )
        raise HTTPException(401, "Invalid email or password")
    
    user_id = user["id"]
    
    # Verify password
    if not pwd_context.verify(password, user["password_hash"]):
        await db.execute(
            """INSERT INTO login_audits
               (user_id, email, success, failure_reason, created_at)
               VALUES ($1, $2, FALSE, 'invalid_password', NOW())""",
            user_id, email_lower,
        )
        raise HTTPException(401, "Invalid email or password")
    
    # Check if account is active
    if not user["is_active"]:
        await db.execute(
            """INSERT INTO login_audits
               (user_id, email, success, failure_reason, created_at)
               VALUES ($1, $2, FALSE, 'account_deactivated', NOW())""",
            user_id, email_lower,
        )
        raise HTTPException(403, "Account deactivated. Contact HR.")
    
    # Check onboarding status
    employee = await db.fetchrow(
        "SELECT onboarding_status FROM employees WHERE user_id=$1",
        user_id,
    )
    if employee and employee["onboarding_status"] != "completed":
        await db.execute(
            """INSERT INTO login_audits
               (user_id, email, success, failure_reason, created_at)
               VALUES ($1, $2, FALSE, 'onboarding_incomplete', NOW())""",
            user_id, email_lower,
        )
        raise HTTPException(
            403,
            f"Onboarding incomplete. Status: {employee['onboarding_status']}. Contact HR."
        )
    
    # Log successful login
    await db.execute(
        """UPDATE users SET last_login = NOW() WHERE id = $1""",
        user_id,
    )
    await db.execute(
        """INSERT INTO login_audits
           (user_id, email, success, failure_reason, created_at)
           VALUES ($1, $2, TRUE, NULL, NOW())""",
        user_id, email_lower,
    )
    
    logger.info("Login success: user_id=%s role=%s", user_id, user["role"])
    
    return {
        "user_id": user_id,
        "email": user["email"],
        "full_name": user["full_name"],
        "role": user["role"],
        "must_reset_password": user["must_reset_password"] or False,
    }